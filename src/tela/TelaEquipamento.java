    private void jbEquipamentoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEquipamentoExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jtEquipamentoTabela.getSelectedRow();
        int codigoEquipamento =(int) jtEquipamentoTabela.getValueAt(linhaSelecionada, 0);
        if (ControllerEquipamento.excluirEquipamentoController(codigoEquipamento)) {
            JOptionPane.showMessageDialog(this, " O produto foi excluido com sucesso.");
            carregarEquipamento();
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao excluir o produto.");
        }
    }//GEN-LAST:event_jbEquipamentoExcluirActionPerformed

