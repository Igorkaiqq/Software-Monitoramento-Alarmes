    private void jbEquipamentoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEquipamentoAlterarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = this.jtEquipamentoTabela.getSelectedRow();
        salvarAlteracao = "alterar";
        habilitarCampos(true);
        jbEquipamentoSalvar.setEnabled(true);
        try {
            int codigoEquipamento = (int) this.jtEquipamentoTabela.getValueAt(linhaSelecionada, 0);
            modelEquipamento = ControllerEquipamento.getEquipamentoController(codigoEquipamento);
            this.jtfEquipamentoCodigo.setText(String.valueOf(modelEquipamento.getIdEquipamento()));
            this.jtfEquipamentoNome.setText(modelEquipamento.getEquipamento_nome());
            this.jtfEquipamentoDescricao.setText(modelEquipamento.getEquipamento_descricao());
            this.jcEquipamentoTipo.setSelectedItem(modelEquipamento.getEquipamento_tipo());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " Erro ao alterar o Equipamento.");
        }
    }//GEN-LAST:event_jbEquipamentoAlterarActionPerformed

    private void jbEquipamentoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEquipamentoPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.jtEquipamentoTabela.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.jtEquipamentoTabela.setRowSorter(classificador);
        String textoPesquisado = jtfEquipamentoPesquisar.getText();
        classificador.setRowFilter(RowFilter.regexFilter(textoPesquisado.toLowerCase(), 2));

    }//GEN-LAST:event_jbEquipamentoPesquisarActionPerformed

