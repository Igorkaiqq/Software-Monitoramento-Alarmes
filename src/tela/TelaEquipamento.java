    private void limparcampos(){
        jtfEquipamentoNome.setText("");
        jtfEquipamentoDescricao.setText("");
  
            
    }
    private void habilitarCampos(boolean condicao){
        jtfEquipamentoNome.setEnabled(condicao);
        jtfEquipamentoDescricao.setEnabled(condicao);
        jcEquipamentoTipo.setEnabled(condicao);
        
    }
    
        private void carregarEquipamento(){
        listaModelEquipamento = ControllerEquipamento.getListaEquipamentoController();
        DefaultTableModel model = (DefaultTableModel) jtEquipamentoTabela.getModel();
        model.setNumRows(0); 
        int contador = listaModelEquipamento.size();
        for (int i = 0; i < contador; i++) {
            model.addRow(new Object[]{
                listaModelEquipamento.get(i).getIdEquipamento(),
                listaModelEquipamento.get(i).getEquipamento_nome(),  
                listaModelEquipamento.get(i).getEquipamento_descricao(),  
                listaModelEquipamento.get(i).getEquipamento_tipo(),  
                datas.formatarData(listaModelEquipamento.get(i).getEquipamento_cadastro()),
               
            });
            
        }
        
    }
    
        private void funcoesDoPrograma(){
        jtfEquipamentoCodigo.setEnabled(false);
        jtfEquipamentoCodigo.setToolTipText("Preenchimento automático.");
    }
        
        private void salvarEquipamento(){
            try {
            modelEquipamento.setEquipamento_cadastro(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {

        }
        modelEquipamento.setEquipamento_nome(this.jtfEquipamentoNome.getText()); 
        modelEquipamento.setEquipamento_descricao(this.jtfEquipamentoDescricao.getText()); 
        modelEquipamento.setEquipamento_tipo((String) this.jcEquipamentoTipo.getSelectedItem());
        if (ControllerEquipamento.salvarEquipamentoController(modelEquipamento)>0) {
            JOptionPane.showMessageDialog(this, " O Equipamento foi registrado com sucesso.");
            carregarEquipamento();
            limparcampos();
            habilitarCampos(false);
            jbEquipamentoSalvar.setEnabled(false);
            jbEquipamentoAlterar.setEnabled(true);
            jbEquipamentoExcluir.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao registrar o Equipamento.");
        }
    }
        
        private void alterarEquipamento(){
        modelEquipamento.setEquipamento_nome(this.jtfEquipamentoNome.getText()); 
        modelEquipamento.setEquipamento_descricao(this.jtfEquipamentoDescricao.getText()); 
        modelEquipamento.setEquipamento_tipo((String) this.jcEquipamentoTipo.getSelectedItem());
        if (ControllerEquipamento.atualizarEquipamentoController(modelEquipamento)) {
            JOptionPane.showMessageDialog(this, " O Equipamento foi alterado com sucesso.");
            carregarEquipamento();
            limparcampos();
            habilitarCampos(false);
            
        }else{
            JOptionPane.showMessageDialog(this, " Erro ao alterar o Equipamento.");
        }
    }
        
